
public class Rota {
	// criando um metodo ir que recebe a interface de Automovel e instancia o objeto automovel
	public void ir(Automovel automovel) {
		
		// utilizando os metodos 
		automovel.virarDireita();
		automovel.virarEsquerda();
		automovel.acelerar();
		automovel.virarDireita();
		
	}
}
