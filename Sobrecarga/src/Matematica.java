public class Matematica {
// metodo somar recebe dois parametros inteiros
	int somar(int x, int y) {
	return x+y;
	
	}
	
// metodo somar recebe tres parametros
	int somar(int x, int y, int z) {
		
		return x+y+z;
		
	}
	
// metodo somar recebe dois parametros em  double
	double somar(double x, double y) {
		
		return x+y;
			
		
	}
	
	
}



