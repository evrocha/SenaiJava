
public class Calculadora {

	public static void main(String[] args) {
		double n1 = 5;
		double n2 = 2;
		double resulS; // soma
		double resulSu; //subttracao
		double resultM; //multiplicacao
		double resulD; //divisao

			resulS = n1 + n2;
			System.out.println("O resultado da soma é: " + resulS);

			resulSu = n1 - n2;
			System.out.println("O resultado da subtração é: " + resulSu);
			
			resultM = n1 * n2;
			System.out.println("O resultado da multiplicação é: " + resultM);
			
			resulD = n1/n2;
			System.out.println("O resultado da divisão é: " + resulD);
	
	}

}
